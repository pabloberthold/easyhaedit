export function getActionExplanation(actionText) {
  if (!actionText || !actionText.trim()) return null
  const parts = actionText.trim().split(/\s+/)
  const key = parts[0]
  return HTTP_REQ_EXPLANATIONS[key] || null
}

export function getAclExplanation(criterionText) {
  if (!criterionText) return null
  const key = criterionText.trim().split(/\s+/)[0]
  return ACL_EXPLANATIONS[key] || null
}

export function getOptionExplanation(optionText) {
  if (!optionText) return null
  return OPTIONS_EXPLANATIONS[optionText] || null
}

